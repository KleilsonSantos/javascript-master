/**
 * Declarando variáveis
 */
let number1 = 10
let number2 = 20
let certo = true
let errado = false

/**
 * Declarando uma condição com && -> E
 */
if (number1 >= 10 && number2 >= 20) {
    console.log(certo)
} else {
    console.log(errado)
}

/**
 * Declarando uma condição com || -> OU
 */
if (number1 <= 10 && number2 >= 20) {
    console.log(certo)
} else {
    console.log(errado)
}