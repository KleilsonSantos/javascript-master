/**
 * Operadores de igualdade
 */
/** 
 *Igualdade restríta
 */
console.log('Igualdade restríta')
console.log(1 === 1)
console.log('carro' === 'carro')
console.log(12.5 === 12.7)
console.log('m' === 'm')

/** 
 * Igualdade solta
 */
console.log('Igualdade solta')
console.log(11 == 1)
console.log('carr' == 'carro')
console.log('12.5' == 12.7)
console.log('m' == 1)